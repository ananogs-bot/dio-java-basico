package Operadores;

public class Operadores2 {

	public static void main(String[] args) {
		
		int numero = 5;
		
		//x repetição
		numero++; // é igual a número + 1
		
		numero += 1; // é igual a anterior
		
		System.out.println(numero++); //adiciona 1 depois 
		
		System.out.println(++numero); //adiciona 1 antes
	}

}
