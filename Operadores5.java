package Operadores;

public class Operadores5 {

	public static void main(String[] args) {
		
		boolean cond1 = true;
		
		boolean cond2 = true;
		
		if(cond1 && cond2) {
			System.out.println("as duas são verdadeiras");
		}
		
		
		if(cond1 || cond2) {
			System.out.println("uma das duas é verdadeira");
		}
	}

}
